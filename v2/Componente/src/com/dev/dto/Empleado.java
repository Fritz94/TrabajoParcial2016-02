package com.dev.dto;

public class Empleado {
	
	private int ValorizacionEmpleado;
	private int idEmpleado;
	private String nombrePersona;
	private String apellidoPersona;
	private int numDNI;
	private String DireccionEmpleado;
	private String CorreoElectronicoEmpleado;
	private String numTelefonico;
	public int getValorizacionEmpleado() {
		return ValorizacionEmpleado;
	}
	public void setValorizacionEmpleado(int valorizacionEmpleado) {
		ValorizacionEmpleado = valorizacionEmpleado;
	}
	public int getIdEmpleado() {
		return idEmpleado;
	}
	public void setIdEmpleado(int idEmpleado) {
		this.idEmpleado = idEmpleado;
	}
	public String getNombrePersona() {
		return nombrePersona;
	}
	public void setNombrePersona(String nombrePersona) {
		this.nombrePersona = nombrePersona;
	}
	public String getApellidoPersona() {
		return apellidoPersona;
	}
	public void setApellidoPersona(String apellidoPersona) {
		this.apellidoPersona = apellidoPersona;
	}
	public int getNumDNI() {
		return numDNI;
	}
	public void setNumDNI(int numDNI) {
		this.numDNI = numDNI;
	}
	public String getDireccionEmpleado() {
		return DireccionEmpleado;
	}
	public void setDireccionEmpleado(String direccionEmpleado) {
		DireccionEmpleado = direccionEmpleado;
	}
	public String getCorreoElectronicoEmpleado() {
		return CorreoElectronicoEmpleado;
	}
	public void setCorreoElectronicoEmpleado(String correoElectronicoEmpleado) {
		CorreoElectronicoEmpleado = correoElectronicoEmpleado;
	}
	public String getNumTelefonico() {
		return numTelefonico;
	}
	public void setNumTelefonico(String numTelefonico) {
		this.numTelefonico = numTelefonico;
	}
	
		
	

}
